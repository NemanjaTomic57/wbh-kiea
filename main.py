@dataclass
class Agent:
    id: int
    x: int
    y: int
    speed: int
    capacity: int
    battery: int

class StandardAgent(Agent):
    def __init__(self, id: int, x: int, y: int):
        super().__init__(
            id=id,
            x=x,
            y=y,
            speed=1,
            capacity=5,
            battery=100
        )

class ExpressAgent(Agent):
    def __init__(self, id: int, x: int, y: int):
        super().__init__(
            id=id,
            x=x,
            y=y,
            speed=2,
            capacity=3,
            battery=80
        )

def print_map(map_data):
    for row in map_data:
        print(" ".join(row))

def main():
    print_map(MAP)

if __name__ == "__main__":
    main()
